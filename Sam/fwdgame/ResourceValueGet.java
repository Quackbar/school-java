public class ResourceValueGet extends ResourceValue
{
	protected boolean build = false;
	protected boolean buildC = false;
	protected boolean gtg = false;
	protected boolean namec = false;
	protected String name = "City17";
	public void setBuild(boolean buil)
	{
		build = buil;
	}
	public boolean getBuild()
	{
		return build;
	}
	public void setBuildC(boolean builc)
	{
		buildC = builc;
	}
	public boolean getBuildC()
	{
		return buildC;
	}

	public void setName(String nam)
	{
		name = nam;
	}
	public String getName()
	{
		return name;
	}
	public void setGTG(boolean gt)
	{
		gtg = gt;
	}
	public boolean getGTG()
	{
		return gtg;
	}

	public void setNamec(boolean nc)
	{
		namec = nc;
	}
	public boolean getNamec()
	{
		return namec;
	}
}